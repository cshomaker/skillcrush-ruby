weather = 'overcast'

case weather
when 'sunny'
  puts "It is shorts and tank weather!"
when 'overcast'
  puts "Layering is always the best choice."
when 'rainy'
  puts "Where did the peacoat and umbrella go?"
when 'snowing'
  puts "Who cares… but make sure to finish with a puffy jacket and grab a sled!"
else
  puts "I can’t decide. You are on your own."
end
